// Verifies this round's skill-description + skill-icon work: every skill in
// every class tree has a description and an icon key that resolves to a
// real generated texture, the Character screen's Skills tab renders icons
// and a tap-to-inspect detail panel, and the HUD skill wheel shows icons.
import { chromium } from 'playwright';
import { mkdirSync, readFileSync } from 'node:fs';

const BASE = 'http://localhost:4173';
const OUT = './smoke-shots-skillicons';
mkdirSync(OUT, { recursive: true });

// --- 1. Data integrity: every skill has a name, a rich effect description,
// and (for non-passive-only edge cases, all of them here) an icon key that
// exists in the icon shape registry. skills.js has no Phaser dependency so
// it imports cleanly in plain Node; skillicons.js does (Graphics/textures),
// so its shape keys are read straight from source text instead of imported.
const skillsMod = await import('../src/data/skills.js');
const iconsSrc = readFileSync(new URL('../src/fx/skillicons.js', import.meta.url), 'utf8');
const shapesBlock = iconsSrc.match(/const SHAPES = \{([\s\S]*?)\n\};/)[1];
const iconKeys = new Set([...shapesBlock.matchAll(/^\s*(\w+):/gm)].map((m) => m[1]));
console.log('icon shapes registered:', iconKeys.size);
let missingIcon = [];
let shortDesc = [];
for (const classId of skillsMod.classIds()) {
  for (const def of skillsMod.getTree(classId)) {
    if (!def.icon || !iconKeys.has(def.icon)) missingIcon.push(`${classId}/${def.id} -> ${def.icon}`);
    if (!def.effect || def.effect.length < 60) shortDesc.push(`${classId}/${def.id} (${def.effect?.length ?? 0} chars)`);
  }
}
console.log('skills missing a valid icon:', missingIcon.length ? missingIcon.join(', ') : 'none');
console.log('skills with a too-short description:', shortDesc.length ? shortDesc.join(', ') : 'none');
const dataOk = missingIcon.length === 0 && shortDesc.length === 0;

const browser = await chromium.launch({ executablePath: '/opt/pw-browsers/chromium', args: ['--no-sandbox'] });
const ctx = await browser.newContext({ viewport: { width: 800, height: 450 }, hasTouch: true });
const page = await ctx.newPage();
const errors = [];
page.on('pageerror', (e) => errors.push('PAGEERROR: ' + e.message));
page.on('console', (m) => { if (m.type() === 'error') errors.push('CONSOLE: ' + m.text()); });

await page.goto(BASE, { waitUntil: 'networkidle' });
await page.waitForTimeout(1500);

// --- 2. Icon textures actually exist once generated in a real scene ---
const texturesOk = await page.evaluate(async () => {
  window.__game.scene.start('Creation');
  await new Promise((r) => setTimeout(r, 300));
  const scene = window.__game.scene.getScene('Creation');
  scene.kindredId = 'vanyar';
  scene.startTraining({ id: 'warrior' });
  await new Promise((r) => setTimeout(r, 800));
  const world = window.__game.scene.getScene('World');
  // textures were generated by ensureSkillIconTextures in Creation/UI scenes already
  const keys = ['bash', 'doubleslash', 'shield', 'spiral', 'dome', 'stormcloud', 'notecrown', 'flowerburst', 'hammerstorm', 'skulldagger', 'hornarmy', 'wildburst'];
  return keys.map((k) => ({ k, exists: world.textures.exists(`skill-icon-${k}`) }));
});
console.log('sample icon textures exist:', JSON.stringify(texturesOk));
const allTexturesOk = texturesOk.every((t) => t.exists);

// --- 3. Character screen Skills tab: icons render + tap-to-inspect works ---
await page.evaluate(() => {
  const world = window.__game.scene.getScene('World');
  world.scene.pause();
  world.scene.launch('Character', { tab: 'skills' });
});
await page.waitForTimeout(500);
await page.screenshot({ path: `${OUT}/01-skills-tab-warrior.png` });

// tap the first skill row (Bash) — row starts right after the header, ~y=100
await page.mouse.click(400, 100);
await page.waitForTimeout(300);
await page.screenshot({ path: `${OUT}/02-skill-detail-bash.png` });
const inspectState = await page.evaluate(() => {
  const c = window.__game.scene.getScene('Character');
  return c.inspectSkill;
});
console.log('inspectSkill after tapping first row:', JSON.stringify(inspectState));
const inspectOk = inspectState?.skillId === 'bash';

// --- 4. HUD skill wheel shows icons (close Character, back to World) ---
await page.evaluate(() => {
  window.__game.scene.stop('Character');
  const world = window.__game.scene.getScene('World');
  world.scene.resume();
});
await page.waitForTimeout(500);
await page.screenshot({ path: `${OUT}/03-hud-skill-wheel.png` });
const wheelIcons = await page.evaluate(() => {
  const ui = window.__game.scene.getScene('UI');
  return ui.skillBtns.filter((b) => b.cont.visible).map((b) => ({ visible: b.icon.visible, texture: b.icon.texture?.key }));
});
console.log('HUD skill wheel icon states:', JSON.stringify(wheelIcons));
const wheelOk = wheelIcons.some((w) => w.visible && w.texture?.startsWith('skill-icon-'));

const allOk = dataOk && allTexturesOk && inspectOk && wheelOk;
console.log('ALL SKILL-ICON CHECKS PASSED:', allOk);

const errorsOut = errors.filter((e) => !e.includes('404'));
console.log('ERRORS:', errorsOut.length ? errorsOut.join('\n') : 'none');
await browser.close();
process.exit(errorsOut.length || !allOk ? 1 : 0);
